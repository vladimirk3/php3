<h2>Задание 1</h2>
<p>1. С помощью цикла while вывести все числа в промежутке от 0 до 100, которые делятся на 3 без остатка.</p>
<?php
//Pадание 1
//1. С помощью цикла while вывести все числа в промежутке от 0 до 100, которые делятся на 3 без остатка.

$x = 0;
while ($x <= 100) {
    if ($x % 3 == 0) {
        echo $x." ";
    }
    $x++;
}
?>

